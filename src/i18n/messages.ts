export const catalogs: Record<import("./locale").Locale, Record<MessageKey, string>> = {
  "zh-CN": zhCN,
  en,
  "zh-TW": zhTW,
};

export type TranslateParams = Record<string, string | number>;

export function translate(
  locale: import("./locale").Locale,
  key: MessageKey,
  params?: TranslateParams,
): string {
  const template = catalogs[locale][key] ?? zhCN[key] ?? key;
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (match, name: string) => {
    const value = params[name];
    if (value === undefined || value === null) return match;
    return String(value);
  });
}

export function messageKeyCount(): number {
  return Object.keys(zhCN).length;
}
